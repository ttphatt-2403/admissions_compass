export const getUniversityLogo = (
  universityId?: string,
  shortName?: string
): string | undefined => {
  const normalizedId = (universityId || "").trim().toLowerCase();
  const logoFromId = normalizedId ? universityLogoById[normalizedId] : "";
  if (logoFromId) return logoFromId;

  // Fallback for ids like "fpt-university", "rmit-vietnam"
  if (normalizedId) {
    const prefix = normalizedId.split("-")[0];
    const logoFromPrefix = universityLogoById[prefix];
    if (logoFromPrefix) return logoFromPrefix;
  }

  const logoFromShortName = shortName ? universityLogoByShortName[shortName] : "";
  if (logoFromShortName) return logoFromShortName;

  // Last fallback: shortName -> lowercase id key (e.g. DTU -> "dtu")
  const normalizedShortName = (shortName || "").trim().toLowerCase();
  if (normalizedShortName) {
    const logoFromShortNameAsId = universityLogoById[normalizedShortName];
    if (logoFromShortNameAsId) return logoFromShortNameAsId;
  }

  return undefined;
};
